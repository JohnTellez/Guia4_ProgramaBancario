    public EstadoCuenta(String estado) {
        this.cuenta = new Datos();
        this.estado = estado;
        initComponents();
        if (estado.equals("activa")){
            jLabelTitulo.setText("CUENTAS ACTIVAS");
            btnAceptar.setText("CERRAR");
        }
        if (estado.equals("inactiva")){
            jLabelTitulo.setText("CUENTAS INACTIVAS");
            btnAceptar.setText("ACTIVAR");
        }
        this.cargar_cuentas();
    }

    private void cargar_cuentas(){
        try{
            this.datos = this.datos_servicio.recuperarEstado(Conexion.obtener(), estado);
            DefaultTableModel dtm = (DefaultTableModel) tabla_estado_cuentas.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.datos.size(); i++){
                dtm.addRow(new Object[]{
                    this.datos.get(i).getNum_cuenta(),
                    this.datos.get(i).getNombre(),
                        this.datos.get(i).getTipo()
                });
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Eerror, no se han recuperado las cuentas");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Error, no se han recuperado las cuentas");
        }
    }

