    private void guardar (Datos dato){
        this.cuenta = dato;
        if (estado.equals("inactiva")){
            this.cuenta.setEstado("activa");
        }
        else if (estado.equals("activa")){
            this.cuenta.setEstado("inactiva");
        }
        try{
            this.datos_servicio.guardar(Conexion.obtener(), this.cuenta);
            EstadoCuenta.this.dispose();
            EstadoCuenta estadoCuenta = new EstadoCuenta(estado);
            estadoCuenta.setVisible(true);
            estadoCuenta.setLocationRelativeTo(null);
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Error, no se han recuperado las cuentas");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Error, no se han recuperado las cuentas");
        }
    }
